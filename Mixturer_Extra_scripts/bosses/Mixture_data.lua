    for uu in ipairs(v) do
        local vv = v[uu]
        if vv.check then
            if type(vv.check) == 'number' then     --要先拷贝
                for uk,vk in pairs(v[vv.check]) do
                    vv[uk] = vv[uk] or vk
                end
            elseif type(vv.check) == 'table' then
                local vc = (extra_data[vv.check.v1] or {})[vv.check.v2] or {}
                for uk,vk in pairs(vc) do
                    vv[uk] = vv[uk] or vk
                end
            end
        end
        for uuu,vvv in pairs(vv) do
            if uuu == 'check' and type(vvv) == 'number' then
            else
                item[u][uu][uuu] = vvv
            end
        end
    end
end

